package conditionalstatement;

public class AgeIfElse {

	public static void main(String[] args) {
		int Virat=25;
		int Gill=16;
		
		if(Virat>=18) {
			System.out.println("Virat is an adult");
		}
		else {
			System.out.println("Virat is a teenager");
		}
		
		if(Gill>=18) {
			System.out.println("Gill is an adult");
		}
		else {
			System.out.println("Gill is a teenager");
		}
	}
}
